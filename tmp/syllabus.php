<h4>Goals</h4>
<div class="indent1">
    <p>This course has two primary goals. First, you will study digital media and some of the techniques that are used to digitally represent photo, sound, and text files. Second, the course aims to teach a bit about how to write computer programs. Programming is the way that computer scientists express their ideas and implement solutions to problems. Even if you never program for a living, you may find situations in your career where you would like to be able to make a tool do something that it doesn’t yet do. More and more applications are allowing this kind of “end user programming.” We hope that you leave this course with a sense of what programs can do and of how you can write programs to express ideas.
    
    <p>Media computation is the primary theme in this course. Computing with images, sound, and text offers a rich body of problems to solve that will bring us into contact with many of the fundamental ideas of computer science: representing and transforming data; design, analysis, and experimentation; and the thrill of solving problems in any domain of human thought. 
    
    <p>By the end of the term, you should feel comfortable:
    <ol>
        <li>writing basic Python programs to manipulate images, sound, and text;
        <li>thinking about problems in terms of representing and transforming data.
    </ol>
</div>

