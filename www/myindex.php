<h3>About me</h3>

<img src="images/sergey.jpg" class="float-r image1 img-resp" alt="Sergey Golitsynskiy">

<p>I am an Assistant Professor in the Interactive Digital Studies program at the <a href="http://uni.edu">University of Northern Iowa</a>. 
I teach courses in the <a href="http://uni.edu/commstudies">Department of Communication Studies</a> and the <a href="http://cs.uni.edu">Department of Computer Science</a>. 
I received my Ph.D. from the <a href="http://umd.edu">University of Maryland</a> (2013), my MS (2008) and MA (2007) from the <a href="http://uni.edu">University of Northern Iowa</a>, 
and my Diploma (1998) from <a href="http://spbu.ru">St. Petersburg State University, Russia</a>. I joined the Department of Communication Studies at UNI in August 2013.

<p>My interests are spread across the domains of computer science, digital humanities, and news media. As a researcher, I am interested in (a) digital archives and, especially, metadata and user interfaces for exploring large photo collections; and (b) computational analysis of (mostly) media texts. As a programmer, my primary focus has been on content management system architecture and generative programming, mostly in the context of web development; I've also done work on search and information retrieval, computational linguistics and ediscovery. Programming brings me joy, which is why - being an academic - I end up writing code for scholarship and research in a variety of fields. As an educator, I try to convey this excitement of writing code (and learning how to build things) to my students. 


